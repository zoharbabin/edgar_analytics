def test_operating_margin_uses_adjusted_cost_of_revenue():
    """Operating Margin should reflect D&A-in-COGS adjustment."""
    bal_df = pd.DataFrame(
        {"Value": [5000, 2000, 3000]},
        index=["Total assets", "Total liabilities", "Total shareholders' equity"],
    )
    inc_df = pd.DataFrame(
        {"Value": [1000, -500, -100, 200, -80, -50]},
        index=[
            "Net sales", "Cost of sales", "Operating expenses",
            "Net income", "Depreciation and amortization",
            "Depreciation in cost of sales",
        ],
    )
    cf_df = pd.DataFrame(
        {"Value": [400, -60]},
        index=["Cash generated by operating activities", "Capital Expenditures"],
    )

    metrics = compute_ratios_and_metrics(bal_df, inc_df, cf_df)
    adjusted_cogs = 500 - 50
    gross = 1000 - adjusted_cogs
    op_income = gross - 100
    assert metrics["Operating Margin %"] == pytest.approx((op_income / 1000) * 100.0, abs=0.01)
